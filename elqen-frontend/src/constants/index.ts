export const shortenAddress = (address: string | null | undefined, chars = 4): string => {
    if (!address) return "";
    // Ensure address is long enough before substring
    const minLength = (chars * 2) + 2; // e.g., 4*2 + 2 = 10 for "0x...1234"
    if (address.length < minLength) return address; // Return full address if too short to shorten meaningfully
    return `${address.substring(0, chars + 2)}...${address.substring(address.length - chars)}`;
};